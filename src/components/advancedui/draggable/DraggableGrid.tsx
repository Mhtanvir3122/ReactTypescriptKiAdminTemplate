import React, { useEffect, useRef } from "react";
import { Card, Col } from "react-bootstrap";

import Sortable from "sortablejs";

const gridItems = Array.from({ length: 18 }, (_, i) => `Grid-${i + 1}`);

const DraggableGrid: React.FC = () => {
  const gridListRef = useRef<HTMLUListElement | null>(null);
  useEffect(() => {
    if (gridListRef.current) {
      new Sortable(gridListRef.current, {
        swap: true,
        swapClass: "highlight",
        animation: 150,
      });
    }
  }, []);

  return (
    <Col xxl={6}>
      <Card className="equal-card">
        <Card.Header>
          <h5>Draggable Grid</h5>
        </Card.Header>
        <Card.Body>
          <ul className="grid-box-list" ref={gridListRef}>
            {gridItems.map((item, index) => (
              <li key={index}>
                <div className="grid-box">
                  <h6>{item}</h6>
                </div>
              </li>
            ))}
          </ul>
        </Card.Body>
      </Card>
    </Col>
  );
};

export default DraggableGrid;
